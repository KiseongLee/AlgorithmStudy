# 참고(파이썬에서 ->의미)

# def funName(x: str, y: float = 6.5) --> int:
#     return x + y

# value = funName(3)
# print(value)
# 함수명 : def 다음에 나와있는 funName
# x : str  콜론(:) 다음에 적어주는 것은 매개변수에 할당되는 형식을 말한다.
# y : float = 6.5 초기값을 콜론다음에 해당 형식 뒤에 = 하고 값을 적어줄 수 있다. (변수 : 형식 = 기본값)
# -> int : 이거의 뜻은 이 함수가 반환해주는 값의 형식이 int라는 것을 의미한다!
